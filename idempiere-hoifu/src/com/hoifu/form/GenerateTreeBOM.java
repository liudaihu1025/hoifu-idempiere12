	/**
	 * 删除节点
	 */
	private void action_deleteNode() {
		if (m_selectedNode == null) {
			Dialog.warn(0, "请先选择一个节点");
			return;
		}

		ProductData productData = m_selectedNode.getProductData();

		// 不能删除根节点
		if (productData.parentId == null) {
			Dialog.warn(0, "不能删除根节点");
			return;
		}

		// 检查是否有子节点
		if (hasChildren(productData.id)) {
			Dialog.warn(0, "该节点有子节点,请先删除子节点");
			return;
		}

		// 确认删除
		Dialog.ask(0, "确认删除", "确定要删除节点 '" + productData.name + "' 吗?", new Callback<Boolean>() {
			@Override
			public void onCallback(Boolean result) {
				if (result) {
					performDelete(productData);
				}
			}
		});
	}

	/**
	 * 执行删除操作
	 */
	private void performDelete(ProductData productData) {
		// 虚拟节点直接从内存移除
		if (productData.isVirtual) {
			productMap.remove(productData.id);
		} else {
			// 真实节点标记为删除(但当前版本不支持删除真实节点)
			Dialog.warn(0, "不能删除真实BOM节点");
			return;
		}

		// 从树中移除节点
		SimpleTreeNode parent = (SimpleTreeNode) m_selectedNode.getParent();
		if (parent != null) {
			parent.remove(m_selectedNode);
		}

		// 刷新树
		try {
			rebuildTreeFromMemory();
		} catch (Exception e) {
			log.log(Level.SEVERE, "刷新树失败", e);
		}

		Dialog.info(0, "节点已删除,请保存以确认更改");
	}

