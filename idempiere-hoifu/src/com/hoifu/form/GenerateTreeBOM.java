	/**
	 * 产品数据类
	 */
	private static class ProductData implements Cloneable {
		// 核心标识
		int id; // 节点唯一ID(正数=真实物料ID, 负数=虚拟节点临时ID)
		String name; // 显示名称
		Integer parentId; // 父节点ID
		int sortOrder; // 排序序号
		String unit; // 计量单位

		// 虚拟节点专用字段
		boolean isVirtual; // true=虚拟节点(需要创建BOM关系), false=真实节点(已有BOM关系)
		boolean isLinkExisting; // true=选择已有物料, false=创建新物料(仅虚拟节点使用)
		Integer linkedProductId; // 选择的物料ID(仅当isLinkExisting=true时使用)

		// BOM相关
		String componentType; // 工序类型
		String description; // 描述(仅真实节点从数据库加载)

		// 缓存MPPProductBOM的自定义字段
		Map<String, Object> bomCustomFields = new HashMap<>();

		// 缓存M_Product的自定义字段
		Map<String, Object> productCustomFields = new HashMap<>();

		ProductData(int id, String name, String description, String unit, Integer parentId, int sortOrder) {
			this.id = id;
			this.name = name;
			this.description = description;
			this.unit = unit;
			this.parentId = parentId;
			this.sortOrder = sortOrder;
			this.isVirtual = false;
			this.isLinkExisting = false;
			this.linkedProductId = null;
			this.componentType = null;
		}

		@Override
		protected ProductData clone() {
			ProductData cloned = new ProductData(id, name, description, unit, parentId, sortOrder);
			cloned.isVirtual = this.isVirtual;
			cloned.componentType = this.componentType;
			cloned.isLinkExisting = this.isLinkExisting;
			cloned.linkedProductId = this.linkedProductId;
			// 复制自定义字段缓存
			cloned.bomCustomFields.putAll(this.bomCustomFields);
			cloned.productCustomFields.putAll(this.productCustomFields);
			return cloned;
		}
	}

	/**
	 * 树节点类
	 */
	class SimpleTreeNode extends DefaultTreeNode<Object> {
		private static final long serialVersionUID = 1L;
		private ProductData data;

		public SimpleTreeNode(ProductData data, List<TreeNode<Object>> children) {
			super(data, children);
			this.data = data;
		}

		public SimpleTreeNode(ProductData data) {
			super(data);
			this.data = data;
		}

		public ProductData getProductData() {
			return data;
		}

		@Override
		public String toString() {
			ProductData data = getProductData();

			if (!data.isVirtual) {
				// 真实节点: 物料编码_物料名称[单位]
				return data.name + " [" + data.unit + "]";
			} else {
				// 虚拟节点: 使用生成的名称
				return data.name;
			}
		}
	}

	/**
	 * 处理树节点拖拽事件
	 */
	private class BOMTreeOnDropListener implements EventListener<Event> {

		@Override
		public void onEvent(Event event) throws Exception {
			if (event instanceof DropEvent) {
				DropEvent de = (DropEvent) event;
				if (de.getDragged() != de.getTarget()) {
					Treeitem src = (Treeitem) ((Treerow) de.getDragged()).getParent();
					Treeitem target = (Treeitem) ((Treerow) de.getTarget()).getParent();

					SimpleTreeNode srcNode = (SimpleTreeNode) src.getValue();
					SimpleTreeNode targetNode = (SimpleTreeNode) target.getValue();

					moveNode(srcNode, targetNode);
				}
			}
		}

		/**
		 * 移动节点
		 */
		private void moveNode(SimpleTreeNode movingNode, SimpleTreeNode toNode) {
			if (movingNode == toNode) {
				return;
			}

			ProductData movingData = movingNode.getProductData();

			// 只有根节点不能移动
			if (movingData.parentId == null) {
				Dialog.warn(0, "不能移动根节点");
				return;
			}

			// 不能将节点移动到自己的子节点下
			if (isDescendant(toNode, movingNode)) {
				Dialog.warn(0, "不能将节点移动到自己的子节点下");
				return;
			}

			// 直接显示移动菜单,让用户选择
			showMoveMenu(movingNode, toNode);
		}

		/**
		 * 显示移动选项菜单
		 */
		private void showMoveMenu(SimpleTreeNode movingNode, SimpleTreeNode toNode) {
			Object rawModel = m_tree.getModel();
			if (!(rawModel instanceof SimpleTreeModel)) {
				log.severe("树模型类型不匹配");
				return;
			}
			SimpleTreeModel model = (SimpleTreeModel) rawModel;
			int[] path = model.getPath(toNode);
			Treeitem toItem = m_tree.renderItemByPath(path);

			Menupopup popup = new Menupopup();

			Menuitem insertAfter = new Menuitem("插入到后面");
			insertAfter.addEventListener(Events.ON_CLICK, e -> {
				moveNodeAfter(movingNode, toNode);
				popup.close();
			});
			popup.appendChild(insertAfter);

			Menuitem moveInto = new Menuitem("移入此节点");
			moveInto.addEventListener(Events.ON_CLICK, e -> {
				moveNodeInto(movingNode, toNode);
				popup.close();
			});
			popup.appendChild(moveInto);

			popup.setPage(m_tree.getPage());
			popup.open(toItem.getTreerow());
		}

		/**
		 * 将节点插入到目标节点后面
		 */
		private void moveNodeAfter(SimpleTreeNode movingNode, SimpleTreeNode toNode) {
			Object rawModel = m_tree.getModel();
			if (!(rawModel instanceof SimpleTreeModel)) {
				log.severe("树模型类型不匹配");
				return;
			}
			SimpleTreeModel model = (SimpleTreeModel) rawModel;
			ProductData movingData = movingNode.getProductData();
			ProductData toData = toNode.getProductData();

			// 检查目标节点是否是根节点
			if (toData.parentId == null) {
				Dialog.warn(0, "不能将节点移动到根节点后面");
				return;
			}

			SimpleTreeNode parent = (SimpleTreeNode) model.getParent(toNode);
			ProductData parentData = parent.getProductData();

			// 从原位置移除
			model.removeNode(movingNode);

			// 插入到目标节点后面
			int index = parent.getChildren().indexOf(toNode) + 1;
			model.addNode(parent, movingNode, index);

			// 更新数据(真实节点和虚拟节点都需要更新)
			movingData.parentId = parentData.id;
			movingData.sortOrder = index;

			// 更新同级节点的排序
			updateSiblingsSortOrder(parent);

			// 刷新树
			try {
				rebuildTreeFromMemory();
			} catch (Exception e) {
				log.log(Level.SEVERE, "刷新树失败", e);
			}

		}

		/**
		 * 将节点移入目标节点(作为第一个子节点)
		 */
		private void moveNodeInto(SimpleTreeNode movingNode, SimpleTreeNode toNode) {
			Object rawModel = m_tree.getModel();
			if (!(rawModel instanceof SimpleTreeModel)) {
				log.severe("树模型类型不匹配");
				return;
			}
			SimpleTreeModel model = (SimpleTreeModel) rawModel;

			ProductData movingData = movingNode.getProductData();
			ProductData toData = toNode.getProductData();

			// *** 检查目标节点是否是虚拟根节点 ***
			if (toData.id == 0) {
				Dialog.warn(0, "不能将节点移动到虚拟根节点下");
				return;
			}

			// 从原位置移除
			model.removeNode(movingNode);

			// 移入目标节点
			model.addNode(toNode, movingNode, 0);

			// 更新数据
			movingData.parentId = toData.id;
			movingData.sortOrder = 0;

			// 更新同级节点的排序
			updateSiblingsSortOrder(toNode);

			// 刷新树
			try {
				rebuildTreeFromMemory();
			} catch (Exception e) {
				log.log(Level.SEVERE, "刷新树失败", e);
			}

		}

		/**
		 * 检查 node 是否是 ancestor 的后代
		 */
		private boolean isDescendant(SimpleTreeNode node, SimpleTreeNode ancestor) {
			// 使用 Object 作为中间类型来避免泛型转换问题
			Object rawModel = m_tree.getModel();
			if (!(rawModel instanceof SimpleTreeModel)) {
				log.severe("树模型类型不匹配");
				return false;
			}
			SimpleTreeModel model = (SimpleTreeModel) rawModel;

			SimpleTreeNode parent = (SimpleTreeNode) model.getParent(node);

			while (parent != null) {
				if (parent == ancestor) {
					return true;
				}
				parent = (SimpleTreeNode) model.getParent(parent);
			}

			return false;
		}

		/**
		 * 更新同级节点的排序
		 */
		private void updateSiblingsSortOrder(SimpleTreeNode parent) {
			ProductData parentData = parent.getProductData();

			for (int i = 0; i < parent.getChildCount(); i++) {
				SimpleTreeNode child = (SimpleTreeNode) parent.getChildAt(i);
				ProductData childData = child.getProductData();

				if (childData.sortOrder != i) {
					childData.sortOrder = i;
				}
			}
		}
	}

