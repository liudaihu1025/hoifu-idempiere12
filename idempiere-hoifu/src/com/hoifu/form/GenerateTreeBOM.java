	/**
	 * 加载BOM树
	 */
	private void action_loadBOM() throws Exception {
		// *** 先保存展开状态(在清空之前!) ***
		if (m_tree.getTreechildren() != null) {
			saveExpandedState(m_tree);
		}

		// *** 检查是否有未保存的修改 ***
		if (!pendingChanges.isEmpty() || !pendingDeletes.isEmpty() || !pendingInserts.isEmpty()) {
			Dialog.ask(0, "警告", "有未保存的修改,刷新将丢失这些修改。是否继续?", new Callback<Boolean>() {
				@Override
				public void onCallback(Boolean result) {
					if (result) {
						try {
							doLoadBOM();
						} catch (Exception e) {
							log.log(Level.SEVERE, "加载BOM失败", e);
						}
					}
				}
			});
			return;
		}

		doLoadBOM();
	}

	// *** 实际执行加载逻辑 ***
	private void doLoadBOM() throws Exception {
		// 如果fieldBOM有值,从数据库加载
		if (fieldBOM != null && fieldBOM.getValue() != null) {
			loadBOMFromDatabase();
		}

		// 查找所有根产品
		List<ProductData> rootProducts = new ArrayList<>();
		for (ProductData pd : productMap.values()) {
			if (pd.parentId == null) {
				rootProducts.add(pd);
			}
		}

		if (rootProducts.isEmpty()) {
			log.warning("未找到根产品");
			return;
		}

		// 后续构建树的逻辑保持不变 ...
		rootProducts.sort((a, b) -> Integer.compare(a.sortOrder, b.sortOrder));
		treeInfo.setText("BOM树形结构 - " + rootProducts.size() + " 个产品");

		SimpleTreeNode dummyRoot = new SimpleTreeNode(new ProductData(0, "Root", "", "", null, 0),
				new ArrayList<TreeNode<Object>>());

		for (ProductData rootProduct : rootProducts) {
			SimpleTreeNode rootNode = new SimpleTreeNode(rootProduct, new ArrayList<TreeNode<Object>>());
			dummyRoot.add(rootNode);
			buildTree(rootNode, rootProduct.id);
		}

		try {
			m_tree.setModel(null);
		} catch (Exception e) {
		}

		if (m_tree.getTreecols() != null)
			m_tree.getTreecols().detach();
		if (m_tree.getTreefoot() != null)
			m_tree.getTreefoot().detach();
		if (m_tree.getTreechildren() != null)
			m_tree.getTreechildren().detach();

		Treecols treeCols = new Treecols();
		m_tree.appendChild(treeCols);
		Treecol treeCol = new Treecol();
		treeCols.appendChild(treeCol);

		SimpleTreeModel model = new SimpleTreeModel(dummyRoot);

		// 启用拖拽功能
		model.setItemDraggable(true);

		// 添加拖拽监听器
		model.addOnDropEventListener(new BOMTreeOnDropListener());

		// 在设置模型前预先添加展开路径
		if (!expandedNodeIds.isEmpty()) {
			addOpenPathsToModel(model, dummyRoot);
		}

		m_tree.setPageSize(-1);
		m_tree.setItemRenderer(model);
		m_tree.setModel(model);

		if (expandedNodeIds.isEmpty()) {
			treeExpand.setChecked(false);
		}
	}

	/**
	 * 递归添加展开路径到模型
	 */
	private void addOpenPathsToModel(SimpleTreeModel model, SimpleTreeNode node) {
		if (node.getData() instanceof ProductData) {
			ProductData data = (ProductData) node.getData();
			if (expandedNodeIds.contains(data.id)) {
				int[] path = model.getPath(node);
				model.addOpenPath(path);
			}
		}

		// 递归处理子节点
		for (int i = 0; i < node.getChildCount(); i++) {
			SimpleTreeNode child = (SimpleTreeNode) node.getChildAt(i);
			addOpenPathsToModel(model, child);
		}
	}

	/**
	 * 保存当前树的展开状态
	 */
	private void saveExpandedState(Tree tree) {
		expandedNodeIds.clear();

		if (tree.getTreechildren() == null) {
			return;
		}

		TreeUtils.traverse(tree, new TreeItemAction() {
			@Override
			public void run(Treeitem treeItem) {
				if (treeItem.getValue() instanceof SimpleTreeNode) {
					SimpleTreeNode node = (SimpleTreeNode) treeItem.getValue();
					if (treeItem.isOpen()) {
						expandedNodeIds.add(node.getProductData().id);
					}
				}
			}
		});
	}

	/**
	 * 递归构建树结构
	 */
	private void buildTree(SimpleTreeNode parentNode, int parentId) {
		List<ProductData> children = new ArrayList<>();
		for (ProductData pd : productMap.values()) {
			if (pd.parentId != null && pd.parentId == parentId) {
				children.add(pd);
			}
		}

		children.sort((a, b) -> Integer.compare(a.sortOrder, b.sortOrder));

		for (ProductData child : children) {
			boolean isLeaf = !hasChildren(child.id);
			SimpleTreeNode childNode = isLeaf ? new SimpleTreeNode(child)
					: new SimpleTreeNode(child, new ArrayList<TreeNode<Object>>());

			parentNode.add(childNode);

			if (!isLeaf) {
				buildTree(childNode, child.id);
			}
		}
	}

	/**
	 * 检查产品是否有子节点
	 */
	private boolean hasChildren(int productId) {
		for (ProductData pd : productMap.values()) {
			if (pd.parentId != null && pd.parentId == productId) {
				return true;
			}
		}
		return false;
	}

