	/**
	 * 执行数据库保存操作 - 将虚拟BOM树保存为独立的BOM版本
	 */
	private void performSaveAll() {
		try {
			// 第一步: 验证所有虚拟节点
			for (ProductData pd : productMap.values()) {
				if (!pd.isVirtual)
					continue;
				if (pd.isLinkExisting && (pd.linkedProductId == null || pd.linkedProductId <= 0)) {
					Dialog.error(0, "虚拟节点 " + pd.name + " 未选择物料");
					return;
				}
			}

			// 获取虚拟BOM树的根节点
			ProductData rootNode = null;
			for (ProductData pd : productMap.values()) {
				if (pd.parentId == null) {
					rootNode = pd;
					break;
				}
			}

			if (rootNode == null) {
				Dialog.error(0, "未找到根节点");
				return;
			}

			// 加载根节点物料信息
			MProduct rootProduct = MProduct.get(Env.getCtx(), rootNode.id);
			if (rootProduct == null) {
				Dialog.error(0, "无法加载根节点物料信息");
				return;
			}

			// 为根节点生成 BOM Value,并从中提取统一版本号
			// 格式: 物料编码_0_V_1_流水号
			String rootSerialCode = String.format("%04d", System.currentTimeMillis() % 10000);
			String rootBOMValue = rootProduct.getValue() + "_0_V_1_" + rootSerialCode;

			// 从 rootBOMValue 中提取流水号部分作为统一版本号
			// 格式: v_1_流水号
			String unifiedRevision = "v_1_" + rootSerialCode;

			System.out.println("根节点BOM Value: " + rootBOMValue);
			System.out.println("统一版本号: " + unifiedRevision);

			Map<Integer, Integer> virtualToRealIdMap = new HashMap<>();

			// 按层级排序处理所有节点(包括根节点)
			List<ProductData> allNodes = new ArrayList<>(productMap.values());
			allNodes.sort((a, b) -> {
				int levelA = calculateNodeLevel(a);
				int levelB = calculateNodeLevel(b);
				if (levelA != levelB)
					return Integer.compare(levelA, levelB);
				return Integer.compare(a.sortOrder, b.sortOrder);
			});

			// 第二步: 处理所有虚拟节点,创建物料
			for (ProductData pd : allNodes) {
				if (!pd.isVirtual)
					continue;

				int productId;
				if (pd.isLinkExisting) {
					// 选择已有物料
					productId = pd.linkedProductId;
				} else {
					// 创建新物料
					MProduct newProduct = new MProduct(Env.getCtx(), 0, null);
					newProduct.setValue(pd.name);
					newProduct.setName(pd.name);

					// 从根节点复制必填字段
					newProduct.setC_UOM_ID(rootProduct.getC_UOM_ID());
					newProduct.setM_Product_Category_ID(rootProduct.getM_Product_Category_ID());
					newProduct.setC_TaxCategory_ID(rootProduct.getC_TaxCategory_ID());
					newProduct.setProductType(rootProduct.getProductType());
					newProduct.setIsStocked(rootProduct.isStocked());
					newProduct.setIsPurchased(rootProduct.isPurchased());
					newProduct.setIsSold(rootProduct.isSold());
					if (rootProduct.getM_AttributeSet_ID() > 0) {
						newProduct.setM_AttributeSet_ID(rootProduct.getM_AttributeSet_ID());
					}

					// 设置自定义字段
					for (Map.Entry<String, Object> entry : rootNode.productCustomFields.entrySet()) {
						newProduct.set_ValueOfColumn(entry.getKey(), entry.getValue());
					}

					newProduct.saveEx();

					productId = newProduct.get_ID();
					virtualToRealIdMap.put(pd.id, productId);
				}
			}

			// 第三步: 为所有非叶子节点创建 PP_Product_BOM 和 PP_Product_BOMLine
			for (ProductData pd : allNodes) {
				// 检查是否有子节点
				boolean hasChildren = false;
				for (ProductData child : productMap.values()) {
					if (child.parentId != null && child.parentId.equals(pd.id)) {
						hasChildren = true;
						break;
					}
				}

				if (!hasChildren) {
					continue; // 叶子节点,跳过
				}

				// 确定当前节点的物料ID
				int currentProductId;
				if (pd.isVirtual) {
					if (pd.isLinkExisting) {
						currentProductId = pd.linkedProductId;
					} else {
						currentProductId = virtualToRealIdMap.get(pd.id);
					}
				} else {
					currentProductId = pd.id;
				}

				// 生成BOM编码和名称
				int level = calculateNodeLevel(pd);
				String bomSerialCode;
				String bomValue;

				// 如果是根节点,使用预先生成的 rootBOMValue
				if (pd.parentId == null) {
					bomValue = rootBOMValue;
				} else {
					// 其他节点使用随机流水号
					bomSerialCode = String.format("%04d", new Random().nextInt(10000));
					MProduct currentProduct = MProduct.get(Env.getCtx(), currentProductId);
					bomValue = currentProduct.getValue() + "_" + level + "_V_1_" + bomSerialCode;
				}

				MProduct currentProduct = MProduct.get(Env.getCtx(), currentProductId);
				String bomName = currentProduct.getName() + " BOM";

				// 确定BOMType
				String bomType;
				if (pd.isVirtual && !pd.isLinkExisting) {
					bomType = MPPProductBOM.BOMTYPE_CurrentActive; // 'A'
				} else {
					bomType = MPPProductBOM.BOMTYPE_ProductConfigure; // 'C'
				}

				// 创建 PP_Product_BOM
				MPPProductBOM bom = new MPPProductBOM(Env.getCtx(), 0, null);
				bom.setM_Product_ID(currentProductId);
				bom.setValue(bomValue);
				bom.setName(bomName);
				bom.setRevision(unifiedRevision); // 使用统一版本号(v_1_流水号格式)
				bom.setBOMType(bomType);
				bom.setBOMUse(MPPProductBOM.BOMUSE_Master);

				// 设置自定义字段
				ProductData parentData = productMap.get(pd.parentId);
				if (parentData != null && parentData.bomCustomFields != null) {
					for (Map.Entry<String, Object> entry : parentData.bomCustomFields.entrySet()) {
						bom.set_ValueOfColumn(entry.getKey(), entry.getValue());
					}
				}

				bom.saveEx();

				// 为该节点的所有子节点创建 PP_Product_BOMLine
				int lineNo = 10;
				for (ProductData child : productMap.values()) {
					if (child.parentId == null || !child.parentId.equals(pd.id)) {
						continue;
					}

					// 确定子节点的物料ID
					int childProductId;
					if (child.isVirtual) {
						if (child.isLinkExisting) {
							childProductId = child.linkedProductId;
						} else {
							childProductId = virtualToRealIdMap.get(child.id);
						}
					} else {
						childProductId = child.id;
					}

					// 创建 PP_Product_BOMLine
					MPPProductBOMLine bomLine = new MPPProductBOMLine(bom);
					bomLine.setM_Product_ID(childProductId);
					bomLine.setLine(lineNo);
					bomLine.setQtyBOM(BigDecimal.ONE);
					bomLine.setQtyBatch(Env.ZERO);
					bomLine.setC_UOM_ID(rootProduct.getC_UOM_ID());
					bomLine.setComponentType(child.componentType != null ? child.componentType : "CO");
					bomLine.setIssueMethod("1");
					bomLine.saveEx();

					lineNo += 10;
				}
			}

			Dialog.info(0, "独立BOM树版本已成功保存到数据库!");
			loadBOMFromDatabase();

		} catch (Exception e) {
			log.log(Level.SEVERE, "保存失败", e);
			Dialog.error(0, "保存失败: " + e.getMessage());
		}
	}

