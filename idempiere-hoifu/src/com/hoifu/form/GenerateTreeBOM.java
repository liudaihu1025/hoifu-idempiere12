	/**
	 * 生成半成品名称: 根节点物料编码_层数_序号_流水码
	 */
	private String generateSemiProductName(ProductData pd) {
		String rootValue = getRootProductValue();
		int level = calculateNodeLevel(pd);
		String serialCode = String.format("%04d", System.currentTimeMillis() % 10000);

		if (pd.isLinkExisting && pd.linkedProductId != null && pd.linkedProductId > 0) {
			// 已选择物料: 物料编码_层数_V_1_流水号_物料名称
			MProduct product = MProduct.get(Env.getCtx(), pd.linkedProductId);
			if (product != null) {
				return product.getValue() + "_" + level + "_V_1_" + serialCode + "_" + product.getName();
			}
		}

		// 创建新物料: 根节点物料编码_层数_V_1_流水号
		return rootValue + "_" + level + "_V_1_" + serialCode;
	}

	/**
	 * 获取根节点的物料编码
	 */
	private String getRootProductValue() {
		for (ProductData pd : productMap.values()) {
			if (pd.parentId == null) {
				// 从name中提取物料编码(格式: "编码_名称")
				String[] parts = pd.name.split("_");
				return parts.length > 0 ? parts[0] : "ROOT";
			}
		}
		return "ROOT";
	}

	/**
	 * 计算节点所在层数(根节点为0层)
	 */
	private int calculateNodeLevel(ProductData pd) {
		int level = 0;
		Integer currentParentId = pd.parentId;

		while (currentParentId != null) {
			level++;
			ProductData parent = productMap.get(currentParentId);
			if (parent == null)
				break;
			currentParentId = parent.parentId;
		}

		return level;
	}

	/**
	 * 计算节点在该层的序号
	 */
	private int calculateSequenceInLevel(ProductData pd) {
		int level = calculateNodeLevel(pd);

		// 找出同层的所有节点
		List<ProductData> sameLevelNodes = new ArrayList<>();
		for (ProductData other : productMap.values()) {
			if (calculateNodeLevel(other) == level) {
				sameLevelNodes.add(other);
			}
		}

		// 按sortOrder排序
		sameLevelNodes.sort((a, b) -> Integer.compare(a.sortOrder, b.sortOrder));

		// 找到当前节点的位置
		for (int i = 0; i < sameLevelNodes.size(); i++) {
			if (sameLevelNodes.get(i).id == pd.id) {
				return i + 1; // 序号从1开始
			}
		}

		return 1;
	}

