	@Override
	public void onEvent(Event event) throws Exception {
		// 刷新按钮
		if (event.getTarget().getId().equals(ConfirmPanel.A_REFRESH)) {
			action_loadBOM();
		}
		// 取消按钮
		else if (event.getTarget().getId().equals(ConfirmPanel.A_CANCEL)) {
			dispose();
		}
		// 树展开复选框
		else if (event.getTarget().equals(treeExpand)) {
			expandOrCollapse();
		}
		// 树选择事件
		else if (event.getTarget() instanceof Tree) {
			Treeitem ti = m_tree.getSelectedItem();
			if (ti == null) {
				log.log(Level.WARNING, "GenerateTreeBOM.onEvent treeItem=null");
			} else {
				SimpleTreeNode tn = (SimpleTreeNode) ti.getValue();
				setSelectedNode(tn);
			}
		}
		// 添加子节点按钮
		else if (event.getTarget().equals(btnAddChild)) {
			action_addChild();
		}
		// 删除节点按钮
		else if (event.getTarget().equals(btnDelete)) {
			action_deleteNode();
		}
		// 单选按钮事件
		else if (event.getTarget().equals(radioLinkExisting) || event.getTarget().equals(radioCreateNew)) {
			if (Events.ON_CHECK.equals(event.getName())) {
				onProductTypeChange();
			}
		}
		// 物料选择器事件
		else if (fieldProduct != null && event.getTarget().equals(fieldProduct.getComponent())) {
			System.out.println("=== 通过onEvent监听到物料选择器事件 ===");
			onProductSelected();
		}
		// 保存按钮
		else if (event.getTarget().equals(btnSave)) {
			action_saveAll();
		}
		// 详情字段变更
		else if (event.getTarget().equals(txtName) || event.getTarget().equals(txtType)
				|| event.getTarget().equals(txtDescription) || event.getTarget().equals(txtUnit)
				|| event.getTarget().equals(txtSortOrder)) {
			if (Events.ON_CHANGE.equals(event.getName())) {
				action_saveNodeChanges();
			}
		}
	}

	private void onProductSelected() {
		System.out.println("=== onProductSelected 被调用 ===");
		Integer productId = (Integer) fieldProduct.getValue();
		System.out.println("选择的物料ID: " + productId);

		if (productId == null || productId == 0 || m_selectedNode == null) {
			System.out.println("退出: productId=" + productId + ", m_selectedNode=" + (m_selectedNode != null));
			return;
		}

		ProductData pd = m_selectedNode.getProductData();
		System.out.println("当前节点ID: " + pd.id + ", 原linkedProductId: " + pd.linkedProductId);

		pd.linkedProductId = productId;
		pd.isLinkExisting = true;
		System.out.println("更新后linkedProductId: " + pd.linkedProductId);

		MProduct product = MProduct.get(Env.getCtx(), productId);
		if (product != null) {
			String rootValue = getRootProductValue();
			int level = calculateNodeLevel(pd);
			int sequence = calculateSequenceInLevel(pd);
			String serialCode = String.format("%04d", Math.abs(pd.id));

			pd.name = String.format("%s_%d_%d_%s_%s", rootValue, level, sequence, product.getValue(), serialCode);
			System.out.println("生成的新名称: " + pd.name);

			int selectedNodeId = pd.id;

			try {
				System.out.println("开始刷新树...");
				rebuildTreeFromMemory();
				System.out.println("树刷新完成,开始重新选中节点...");

				Object rawModel = m_tree.getModel();
				if (!(rawModel instanceof SimpleTreeModel)) {
					log.severe("树模型类型不匹配");
				}
				SimpleTreeModel model = (SimpleTreeModel) rawModel;
				DefaultTreeNode<Object> foundNode = findNodeById(model.getRoot(), selectedNodeId);
				if (foundNode != null) {
					System.out.println("找到节点,重新选中...");
					int[] path = model.getPath(foundNode);
					Treeitem ti = m_tree.renderItemByPath(path);
					m_tree.selectItem(ti);
					setSelectedNode((SimpleTreeNode) foundNode);
					System.out.println("节点重新选中完成");
				} else {
					System.out.println("未找到节点!");
				}
			} catch (Exception e) {
				log.log(Level.SEVERE, "刷新树失败", e);
				e.printStackTrace();
			}
		}
	}

	/**
	 * 根据ID查找节点
	 */
	private DefaultTreeNode<Object> findNodeById(TreeNode<Object> root, int targetId) {
		if (root instanceof SimpleTreeNode) {
			SimpleTreeNode node = (SimpleTreeNode) root;
			if (node.getProductData().id == targetId) {
				return node;
			}
		}

		for (int i = 0; i < root.getChildCount(); i++) {
			DefaultTreeNode<Object> found = findNodeById(root.getChildAt(i), targetId);
			if (found != null) {
				return found;
			}
		}

		return null;
	}

